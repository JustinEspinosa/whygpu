package com.github.justinespinosa.textmode.curses.ui;


public enum AnsiColor16 implements Color {
	Black        (0x00,0x00,0x00),
	Red          (0x80,0x00,0x00),
	Green        (0x00,0x80,0x00),
	Yellow       (0x80,0x80,0x00),
	Blue         (0x00,0x00,0x80),
	Magenta      (0x80,0x00,0x80),
	Cyan         (0x00,0x80,0x80),
	Gray         (0xc0,0xc0,0xc0),
	DarkGray     (0x80,0x80,0x80),
	LightRed     (0xff,0x00,0x00),
	LightGreen   (0x00,0xff,0x00),
	LightYellow  (0xff,0xff,0x00),
	LightBlue    (0x00,0x00,0xff),
	LightMagenta (0xff,0x00,0xff),
	LightCyan    (0x00,0xff,0xff),
	White        (0xff,0xff,0xff);

	private RGB color;
	private AnsiColor16(int r,int g,int b){
		color = new RGB(r,g,b,ordinal());
	}
	public int index(){
		return color.index();
	}
	public RGB rgb() {
		return color;
	}
	public ColorDepth depth(){
		return ColorDepth.COL16;
	}
}
